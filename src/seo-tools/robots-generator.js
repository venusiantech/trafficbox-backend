function normalizeDomainInput(domain) {
  const raw = String(domain || "").trim();
  if (!raw) return null;
  // Accept plain domain or full URL.
  if (/^https?:\/\//i.test(raw)) {
    const u = new URL(raw);
    return u.host;
  }
  return raw.replace(/^\/+|\/+$/g, "");
}

function sanitizePathList(paths, fallback) {
  if (!Array.isArray(paths) || paths.length === 0) return fallback;
  const cleaned = paths
    .map((p) => String(p || "").trim())
    .filter(Boolean)
    .map((p) => (p.startsWith("/") ? p : `/${p}`));
  return cleaned.length > 0 ? cleaned : fallback;
}

async function runRobotsGenerator(req, res) {
  if (!req.user || !req.user.id) {
    return res.status(401).json({ error: "Authentication required" });
  }

  const body = req.body || {};
  const domain = normalizeDomainInput(body.domain);
  if (!domain) {
    return res.status(400).json({ detail: "Missing required field: domain" });
  }

  const allowPaths = sanitizePathList(body.allow_paths, ["/"]);
  const disallowPaths = sanitizePathList(body.disallow_paths, [
    "/admin",
    "/private",
    "/api",
  ]);
  const sitemapUrl =
    body.sitemap_url && String(body.sitemap_url).trim()
      ? String(body.sitemap_url).trim()
      : `https://${domain}/sitemap.xml`;

  const crawlDelayRaw = body.crawl_delay;
  const crawlDelay =
    crawlDelayRaw === null || crawlDelayRaw === undefined || String(crawlDelayRaw).trim() === ""
      ? null
      : parseInt(crawlDelayRaw, 10);

  let robots = "# Robots.txt generated by TrafficBoxes SEO Suite\n";
  robots += `# Generated: ${new Date().toISOString().replace("T", " ").replace("Z", " UTC")}\n\n`;
  robots += "User-agent: *\n";

  for (const path of allowPaths) robots += `Allow: ${path}\n`;
  for (const path of disallowPaths) robots += `Disallow: ${path}\n`;

  if (crawlDelay && !Number.isNaN(crawlDelay) && crawlDelay > 0) {
    robots += `\nCrawl-delay: ${crawlDelay}\n`;
  }

  robots += `\nSitemap: ${sitemapUrl}\n`;

  return res.json({
    success: true,
    robots_txt: robots,
    domain,
  });
}

module.exports = { runRobotsGenerator };

